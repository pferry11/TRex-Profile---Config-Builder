/* TRex Profile & Config Builder - scenario wizard logic (pure, no DOM).
 * Builds profile models + runbooks for the two built-in scenarios:
 *   A) two-server run - one box sends (client side), the other receives (server side)
 *   B) connection ramp - low -> mid -> high
 * The UI in js/ui/scenarios.js is a thin form layer over these functions.
 *
 * Verified against the local TRex v3.06 tree:
 *   --astf-server-only and --astf-client-mask exist in the _t-rex-64 binary;
 *   interactive equivalent: start ... --client_mask (trex_astf_client.py:702).
 *   scheduler.rampup_sec idiom: astf/param_sch_rampup.py.
 */
(function (root) {
  'use strict';
  var TB = root.TB = root.TB || {};

  function round3(x) { return Math.round(x * 1000) / 1000; }

  function emptyGlobalsSide() {
    return {
      tcp: { mss: null, rxbufsize: null, txbufsize: null, initwnd: null, no_delay: null,
             do_rfc1323: null, keepinit: null, keepidle: null, keepintvl: null },
      scheduler: { rampupSec: null },
      ipv6: { enable: false, srcMsb: '', dstMsb: '' }
    };
  }

  function astfBase(name, trexVersion, ranges) {
    return {
      kind: 'astf', schemaVersion: 1, trexVersion: trexVersion || '3.06',
      meta: { name: name, description: 'generated by scenario wizard', modified: '' },
      ipGen: {
        client: { start: ranges.clientStart, end: ranges.clientEnd, distribution: 'seq', perCore: null },
        server: { start: ranges.serverStart, end: ranges.serverEnd, distribution: 'seq', perCore: null },
        ipOffset: '1.0.0.0'
      },
      globals: { client: emptyGlobalsSide(), server: emptyGlobalsSide() },
      mode: 'pcap', capList: [], templates: []
    };
  }

  function httpTemplate(cps, port, tgName) {
    return {
      id: 'wiz_' + (port || 80), tgName: tgName || null, cps: cps, assocPort: port || 80, stream: true,
      client: { commands: [
        { op: 'send', payload: { kind: 'httpRequest' } },
        { op: 'recv', bytes: null }
      ] },
      server: { commands: [
        { op: 'recv', bytes: null },
        { op: 'send', payload: { kind: 'httpResponse', bodyBytes: 32768 } }
      ] },
      ipGenOverride: null
    };
  }

  function stlStream(name, pps, isgUsec, ranges, frameSize) {
    return {
      id: 'wiz_' + name, name: name, enabled: true,
      packet: {
        l2: { srcMac: null, dstMac: null },
        vlan: { enabled: false, id: 100, prio: 0 },
        l3: { type: 'ipv4', src: ranges.clientStart, dst: ranges.serverStart, tos: null, ttl: null },
        l4: { type: 'udp', sport: 1025, dport: 12, tcpFlags: null },
        payload: { mode: 'pad', frameSize: frameSize || 512, frameSizeTunable: null, fill: 'x', rawScapy: null }
      },
      mode: { type: 'cont', pps: pps, totalPkts: null, pktsPerBurst: null, ibgUsec: null, count: null },
      isgUsec: isgUsec,
      chain: { selfStart: true, next: null, actionCount: null },
      vm: { cacheSize: null,
        vars: [{ name: 'ip_src', sizeBytes: 4, op: 'inc', min: ranges.clientStart, max: ranges.clientEnd,
                 step: 1, writeTo: 'IP.src', fixChecksum: true }],
        tuple: null },
      flowStats: { type: 'none', pgId: null, addPortId: false }
    };
  }

  function generateProfile(model, now) {
    var gen = TB.gen.resolve(model.trexVersion, model.kind);
    if (!gen) { return null; }
    return gen(model, { now: now });
  }

  function summarySection(model) {
    var sentences = TB.gen.summary ? TB.gen.summary(model) : [];
    if (!sentences.length) { return ''; }
    return '\nWHAT THE PROFILE DOES\n' + sentences.map(function (l) { return '  - ' + l; }).join('\n') + '\n';
  }

  function checklistLines(ranges) {
    return [
      'PRE-FLIGHT CHECKLIST',
      '  [ ] Copy the profile .py file to BOTH boxes.',
      '  [ ] Review /etc/trex_cfg.yaml port_info on BOTH boxes:',
      '      - the receiver box must own/answer the server IP range ' + ranges.serverStart + ' - ' + ranges.serverEnd,
      '        (ip/default_gw entries, or cross-wired dest_mac towards the DUT/peer).',
      '  [ ] Client range ' + ranges.clientStart + ' - ' + ranges.clientEnd + ' and the server range must be',
      '      routable through the device under test.',
      '  [ ] In trex-console, verify ARP/ping resolution before starting.',
      '  [ ] Watch on sender: active flows / CPS / tx-bps.',
      '      Watch on receiver: rx-bps, established sessions.'
    ];
  }

  /* ---------------- Scenario A: two-server send/receive ---------------- */

  /* opts: { mode:'astf'|'stl', name, trexVersion, senderHost, receiverHost,
   *         clientStart, clientEnd, serverStart, serverEnd,
   *         traffic:'http'|'pcap', pcapFile, rate (cps for astf / pps for stl),
   *         cores, mult, durationSec, rampupSec|null, addLatency (stl), now } */
  function buildTwoServer(opts) {
    var errors = [];
    ['clientStart', 'clientEnd', 'serverStart', 'serverEnd'].forEach(function (k) {
      if (!TB.util.isIpv4(opts[k])) { errors.push(k + ' is not a valid IPv4 address.'); }
    });
    if (!(opts.rate > 0)) { errors.push('Rate must be greater than 0.'); }
    if (!(opts.durationSec > 0)) { errors.push('Duration must be greater than 0.'); }
    if (opts.traffic === 'pcap' && !opts.pcapFile) { errors.push('Pcap file path is required for pcap traffic.'); }
    if (errors.length) { return { ok: false, errors: errors }; }

    var name = (opts.name || 'two_server').replace(/[^\w.-]+/g, '_');
    var cores = opts.cores || 4;
    var mult = opts.mult || 1;
    var now = opts.now || TB.util.todayIso();
    var model, runbook;
    var warnings = [];

    if (opts.mode === 'astf') {
      model = astfBase(name, opts.trexVersion, opts);
      if (opts.traffic === 'pcap') {
        model.mode = 'pcap';
        model.capList = [{ file: opts.pcapFile, cps: opts.rate, port: null, sDelayUsec: null, ipGenOverride: null }];
      } else {
        model.mode = 'program';
        model.templates = [httpTemplate(opts.rate, 80, null)];
      }
      if (opts.rampupSec) { model.globals.client.scheduler.rampupSec = opts.rampupSec; }

      if (opts.bidirectional) {
        runbook = [
          'TRex two-server run - BIDIRECTIONAL (both boxes run client AND server roles)',
          'Generated: ' + now + '   Target: TRex v' + (opts.trexVersion || '3.06'),
          'Both boxes load the SAME profile file: ' + name + '.py',
          '',
          'Box A (' + (opts.senderHost || 'box-a') + '):',
          '    cd <trex dir>',
          '    sudo ./t-rex-64 -i --astf --astf-client-mask 0x1 --cfg /etc/trex_cfg.yaml -c ' + cores,
          '    # in a second terminal:',
          '    ./trex-console',
          '    trex> start -f ' + name + '.py -m ' + mult + ' -d ' + opts.durationSec,
          '',
          'Box B (' + (opts.receiverHost || 'box-b') + ') - the SAME commands:',
          '    cd <trex dir>',
          '    sudo ./t-rex-64 -i --astf --astf-client-mask 0x1 --cfg /etc/trex_cfg.yaml -c ' + cores,
          '    ./trex-console',
          '    trex> start -f ' + name + '.py -m ' + mult + ' -d ' + opts.durationSec,
          '',
          'HOW IT WORKS',
          '  - --astf-client-mask 0x1: on EACH box, port 0 initiates connections',
          '    (client role) while the remaining port(s) answer as servers.',
          '  - With both boxes started, connections flow in BOTH directions',
          '    simultaneously: A port 0 -> B server port, and B port 0 -> A server port.',
          '  - Start the two boxes within a few seconds of each other for symmetric load;',
          '    -m scales each box independently (set the same value on both).',
          '  - Each box reports its own client-side CPS/flows; aggregate = sum of both.',
          '',
          checklistLines(opts).join('\n'),
          '',
          'NOTES',
          '  - BOTH boxes must route/answer the server range AND reach it as clients:',
          '    the DUT needs symmetric routing for ' + opts.clientStart + '-' + opts.clientEnd,
          '    and ' + opts.serverStart + '-' + opts.serverEnd + ' on both port pairs.',
          (opts.rampupSec ? '  - rampup_sec=' + opts.rampupSec + ' ramps CPS linearly on each client side.' : null),
          '  - Adjust the hex bitmask if your client ports differ (0x1 = port 0).'
        ].filter(function (l) { return l !== null; }).join('\n') + '\n';
      } else {
      runbook = [
        'TRex two-server run - one sends (client side), one receives (server side)',
        'Generated: ' + now + '   Target: TRex v' + (opts.trexVersion || '3.06'),
        'Both boxes load the SAME profile file: ' + name + '.py',
        '',
        'Box B - RECEIVER / server side (' + (opts.receiverHost || 'receiver') + '):',
        '    cd <trex dir>',
        '    sudo ./t-rex-64 -i --astf --astf-server-only --cfg /etc/trex_cfg.yaml -c ' + cores,
        '',
        'Box A - SENDER / client side (' + (opts.senderHost || 'sender') + '):',
        '    cd <trex dir>',
        '    sudo ./t-rex-64 -i --astf --astf-client-mask 0x1 --cfg /etc/trex_cfg.yaml -c ' + cores,
        '    # in a second terminal:',
        '    ./trex-console',
        '    trex> start -f ' + name + '.py -m ' + mult + ' -d ' + opts.durationSec,
        '',
        checklistLines(opts).join('\n'),
        '',
        'NOTES',
        '  - cps in the profile is the rate at -m 1; scale with -m at start time.',
        (opts.rampupSec ? '  - rampup_sec=' + opts.rampupSec + ' ramps CPS linearly on the client side.' : null),
        '  - --astf-client-mask 0x1 = port 0 acts as client on the sender box;',
        '    adjust the hex bitmask if your client ports differ.'
      ].filter(function (l) { return l !== null; }).join('\n') + '\n';
      }
    } else {
      model = {
        kind: 'stl', schemaVersion: 1, trexVersion: opts.trexVersion || '3.06',
        meta: { name: name, description: 'generated by scenario wizard', modified: '' },
        tunables: [],
        streams: [stlStream('S0', opts.rate, 0, opts, opts.frameSize)]
      };
      if (opts.addLatency) {
        var lat = stlStream('LAT', 1000, 0, opts, 64);
        lat.vm.vars = [];
        lat.flowStats = { type: 'latency', pgId: 1, addPortId: true };
        model.streams.push(lat);
      }

      runbook = [
        'TRex two-server run - STL unidirectional (A transmits, B measures)',
        'Generated: ' + now + '   Target: TRex v' + (opts.trexVersion || '3.06'),
        '',
        'Box B - RECEIVER (' + (opts.receiverHost || 'receiver') + '):',
        '    cd <trex dir>',
        '    sudo ./t-rex-64 -i --stl --cfg /etc/trex_cfg.yaml -c ' + cores,
        '    ./trex-console',
        '    trex> stats                          # watch rx counters',
        '    trex> capture record start --rx 0    # optional packet capture',
        '',
        'Box A - SENDER (' + (opts.senderHost || 'sender') + '):',
        '    cd <trex dir>',
        '    sudo ./t-rex-64 -i --stl --cfg /etc/trex_cfg.yaml -c ' + cores,
        '    ./trex-console',
        '    trex> start -f ' + name + '.py -p 0 -m ' + mult + ' -d ' + opts.durationSec,
        '',
        checklistLines(opts).join('\n'),
        '',
        'NOTES',
        '  - Cross-chassis flow-stat/latency correlation is NOT automatic: the',
        '    receiver box sees raw port counters/captures only.',
        (opts.addLatency ? '  - The latency stream ignores -m; its pps (1000) is fixed in the profile.' : null)
      ].filter(function (l) { return l !== null; }).join('\n') + '\n';
    }

    var r = generateProfile(model, now);
    if (!r) { return { ok: false, errors: ['No generator registered for TRex v' + model.trexVersion + '.'] }; }
    runbook += summarySection(model);
    return {
      ok: true,
      model: model,
      files: r.files.concat([{ name: 'RUNBOOK.txt', language: 'shell', content: runbook }]),
      warnings: warnings.concat(r.warnings)
    };
  }

  /* ---------------- Scenario B: connection ramp low -> mid -> high ---------------- */

  /* opts: { engine:'astf'|'stl', name, trexVersion, stageSec,
   *         stages: [r1, r2, ... rN] (any number, strictly increasing)
   *           - or legacy low/mid/high for a 3-stage ramp,
   *         mechanism:'rampup'|'m_step'|'weighted' (astf only),
   *         traffic:'http'|'pcap', pcapFile,
   *         clientStart..serverEnd (defaults provided), cores, frameSize, now } */
  function buildRamp(opts) {
    var stages = (Array.isArray(opts.stages) && opts.stages.length)
      ? opts.stages.slice()
      : [opts.low, opts.mid, opts.high];
    var errors = [];
    if (stages.length < 2) { errors.push('At least two stages are required.'); }
    if (!stages.every(function (s) { return s > 0; })) { errors.push('All stage rates must be greater than 0.'); }
    for (var si = 1; si < stages.length; si++) {
      if (!(stages[si] > stages[si - 1])) {
        errors.push('Stages must strictly increase: stage ' + (si + 1) + ' (' + stages[si] +
          ') is not above stage ' + si + ' (' + stages[si - 1] + ').');
        break;
      }
    }
    if (!(opts.stageSec > 0)) { errors.push('Stage duration must be greater than 0.'); }
    if (opts.engine === 'astf' && opts.traffic === 'pcap' && !opts.pcapFile) {
      errors.push('Pcap file path is required for pcap traffic.');
    }
    if (errors.length) { return { ok: false, errors: errors }; }

    var name = (opts.name || 'ramp').replace(/[^\w.-]+/g, '_');
    var now = opts.now || TB.util.todayIso();
    var cores = opts.cores || 4;
    var N = stages.length;
    var total = opts.stageSec * N;
    var first = stages[0];
    var last = stages[N - 1];
    var ranges = {
      clientStart: opts.clientStart || '16.0.0.1', clientEnd: opts.clientEnd || '16.0.0.255',
      serverStart: opts.serverStart || '48.0.0.1', serverEnd: opts.serverEnd || '48.0.255.255'
    };
    var model, runbook;
    var head = [
      'TRex connection ramp: ' + stages.join(' -> ') +
        (opts.engine === 'astf' ? ' cps' : ' pps') + ', ' + N + ' stages x ' + opts.stageSec + ' s',
      'Generated: ' + now + '   Target: TRex v' + (opts.trexVersion || '3.06'),
      ''
    ];

    if (opts.engine === 'astf') {
      model = astfBase(name, opts.trexVersion, ranges);

      function withTraffic(cpsList, tgNames) {
        if (opts.traffic === 'pcap') {
          model.mode = 'pcap';
          model.capList = cpsList.map(function (c) {
            return { file: opts.pcapFile, cps: round3(c), port: null, sDelayUsec: null, ipGenOverride: null };
          });
        } else {
          model.mode = 'program';
          model.templates = cpsList.map(function (c, i) {
            return httpTemplate(round3(c), 80 + i, tgNames ? tgNames[i] : null);
          });
        }
      }

      /* per-stage deltas: stage 1 full rate, later stages add the difference */
      var deltas = stages.map(function (s, i) { return round3(i === 0 ? s : s - stages[i - 1]); });

      if (opts.mechanism === 'rampup') {
        withTraffic([last]);
        model.globals.client.scheduler.rampupSec = opts.stageSec * (N - 1);
        runbook = head.concat([
          'Mechanism: scheduler.rampup_sec (linear client-side ramp, no plateaus)',
          '  - profile cps = ' + last + ' (the top stage), rampup_sec = ' + (opts.stageSec * (N - 1)),
          '  - CPS grows linearly and reaches the top rate after ' + (opts.stageSec * (N - 1)) + ' s.',
          '',
          'Run (single start):',
          '    sudo ./t-rex-64 -i --astf --cfg /etc/trex_cfg.yaml -c ' + cores,
          '    ./trex-console',
          '    trex> start -f ' + name + '.py -m 1 -d ' + total,
          '',
          'NOTE: this is a smooth linear ramp; if you need discrete stage',
          'plateaus, regenerate with the "-m stepping" mechanism.'
        ]).join('\n') + '\n';
      } else if (opts.mechanism === 'weighted') {
        withTraffic(deltas, stages.map(function (_, i) { return 'stage' + (i + 1); }));
        runbook = head.concat([
          'Mechanism: weighted multi-template mix (APPROXIMATION)',
          '  - ' + N + ' templates with cps ' + deltas.join(' / ') + ' summing to ' + last + ' cps.',
          '  - This is a STEADY mix at the top rate, not a time-based ramp;',
          '    use it as a weighted-traffic preset, or combine with rampup_sec.',
          '',
          'Run:',
          '    sudo ./t-rex-64 -i --astf --cfg /etc/trex_cfg.yaml -c ' + cores,
          '    ./trex-console',
          '    trex> start -f ' + name + '.py -m 1 -d ' + total
        ]).join('\n') + '\n';
      } else { /* m_step - default */
        withTraffic([first]);
        var seq = [
          'Mechanism: -m multiplier stepping (discrete plateaus)',
          '  - profile cps = ' + first + ' at -m 1; each stage rescales with -m.',
          '  - CAVEAT: each stop/start restarts flows between stages.',
          '',
          'Console sequence:',
          '    sudo ./t-rex-64 -i --astf --cfg /etc/trex_cfg.yaml -c ' + cores,
          '    ./trex-console',
          '    trex> start -f ' + name + '.py -m 1 -d ' + total + '        # stage 1: ' + first + ' cps'
        ];
        for (var mi = 1; mi < N; mi++) {
          seq.push('    # wait ' + opts.stageSec + ' s');
          seq.push('    trex> stop');
          seq.push('    trex> start -f ' + name + '.py -m ' + round3(stages[mi] / first) +
            ' -d ' + (opts.stageSec * (N - mi)) + '   # stage ' + (mi + 1) + ': ' + stages[mi] + ' cps');
        }
        runbook = head.concat(seq).join('\n') + '\n';
      }
    } else {
      /* STL: additive staggered-isg continuous streams -> exact plateaus */
      var stageUsec = opts.stageSec * 1000000;
      var streams = [];
      var lines = [];
      stages.forEach(function (s, i) {
        var delta = round3(i === 0 ? s : s - stages[i - 1]);
        streams.push(stlStream('S_stage' + (i + 1), delta, stageUsec * i, ranges, opts.frameSize));
        lines.push('  - S_stage' + (i + 1) + ' : ' + (i === 0 ? '' : '+') + delta +
          ' pps, starts at t=' + (opts.stageSec * i) + ' s' + (i === 0 ? '' : ' (isg)'));
      });
      model = {
        kind: 'stl', schemaVersion: 1, trexVersion: opts.trexVersion || '3.06',
        meta: { name: name, description: 'generated by scenario wizard', modified: '' },
        tunables: [],
        streams: streams
      };
      runbook = head.concat([
        'Mechanism: additive staggered-isg continuous streams (exact plateaus)'
      ]).concat(lines).concat([
        '  - aggregate rate steps ' + stages.join(' -> ') + ' pps.',
        '',
        'Run:',
        '    sudo ./t-rex-64 -i --stl --cfg /etc/trex_cfg.yaml -c ' + cores,
        '    ./trex-console',
        '    trex> start -f ' + name + '.py -p 0 -m 1 -d ' + total,
        '',
        'NOTES',
        '  - -m scales all ' + N + ' streams proportionally (plateaus keep their ratio).',
        '  - Latency streams (if you add any in the builder) ignore -m.'
      ]).join('\n') + '\n';
    }

    var r = generateProfile(model, now);
    if (!r) { return { ok: false, errors: ['No generator registered for TRex v' + model.trexVersion + '.'] }; }
    runbook += summarySection(model);
    return {
      ok: true,
      model: model,
      files: r.files.concat([{ name: 'RUNBOOK.txt', language: 'shell', content: runbook }]),
      warnings: r.warnings
    };
  }

  /* ---------------- Scenario C: connectivity check ---------------- */

  /* Prove L2/L3 resolution and routing before any load test. Console syntax
   * verified against the local v3.06 tree (trex_client.py: ping/arp console
   * APIs; service --off in parsing_opts.py).
   *
   * opts: { name, trexVersion, ping0Dst, ping1Dst, pingCount,
   *         withLatency, latPps, srcIp, dstIp, durationSec, cores, now } */
  function buildConnCheck(opts) {
    var errors = [];
    if (!TB.util.isIpv4(opts.ping0Dst)) { errors.push('Port 0 ping destination is not a valid IPv4 address.'); }
    if (!TB.util.isIpv4(opts.ping1Dst)) { errors.push('Port 1 ping destination is not a valid IPv4 address.'); }
    if (!(opts.pingCount >= 1)) { errors.push('Ping count must be at least 1.'); }
    if (opts.withLatency) {
      if (!TB.util.isIpv4(opts.srcIp)) { errors.push('Latency probe source IP is not a valid IPv4 address.'); }
      if (!TB.util.isIpv4(opts.dstIp)) { errors.push('Latency probe destination IP is not a valid IPv4 address.'); }
      if (!(opts.latPps > 0)) { errors.push('Latency probe pps must be greater than 0.'); }
      if (!(opts.durationSec > 0)) { errors.push('Duration must be greater than 0.'); }
    }
    if (errors.length) { return { ok: false, errors: errors }; }

    var name = (opts.name || 'conn_check').replace(/[^\w.-]+/g, '_');
    var now = opts.now || TB.util.todayIso();
    var cores = opts.cores || 2;
    var count = opts.pingCount || 5;
    var model = null;
    var files = [];
    var warnings = [];

    if (opts.withLatency) {
      var ranges = { clientStart: opts.srcIp, clientEnd: opts.srcIp,
                     serverStart: opts.dstIp, serverEnd: opts.dstIp };
      var probe = stlStream('PROBE', opts.latPps, 0, ranges, 64);
      probe.vm.vars = [];   /* fixed IPs: this is a path check, not a sweep */
      probe.flowStats = { type: 'latency', pgId: 1, addPortId: true };
      model = {
        kind: 'stl', schemaVersion: 1, trexVersion: opts.trexVersion || '3.06',
        meta: { name: name, description: 'generated by scenario wizard', modified: '' },
        tunables: [],
        streams: [probe]
      };
      var r = generateProfile(model, now);
      if (!r) { return { ok: false, errors: ['No generator registered for TRex v' + model.trexVersion + '.'] }; }
      files = r.files;
      warnings = r.warnings;
    }

    var runbook = [
      'TRex connectivity check - prove L2/L3 + routing BEFORE any load test',
      'Generated: ' + now + '   Target: TRex v' + (opts.trexVersion || '3.06'),
      'No traffic profile needed for steps 1-2; everything runs from the console.',
      '',
      'STEP 1 - bring the ports up and resolve next hops (zero packets of load):',
      '    cd <trex dir>',
      '    sudo ./t-rex-64 -i --stl --cfg /etc/trex_cfg.yaml -c ' + cores,
      '    # second terminal:',
      '    ./trex-console',
      '    trex> portattr                     # both links UP?',
      '    trex> service                      # service mode: ports answer ARP/ICMP',
      '    trex> arp -p 0 1                   # resolve gateway / next-hop MACs',
      '    # PASS: both ports show a resolved destination MAC.',
      '',
      'STEP 2 - ICMP ping across the network (proves routing both ways):',
      '    trex> ping -p 0 -d ' + opts.ping0Dst + ' -n ' + count + '     # port 0 -> far side',
      '    trex> ping -p 1 -d ' + opts.ping1Dst + ' -n ' + count + '     # port 1 -> far side',
      '    # PASS: replies with sane RTTs in BOTH directions.',
      '    # The RTT printed per reply is your first latency number.',
      '    trex> service --off                # IMPORTANT: back to normal mode'
    ];

    if (opts.withLatency) {
      runbook = runbook.concat([
        '',
        'STEP 3 - data-plane latency probe (' + opts.latPps + ' pps of 64-byte UDP, ~zero load):',
        '    trex> start -f ' + name + '.py -p 0 -m 1 -d ' + opts.durationSec,
        '    trex> tui                          # press l: Latency window',
        '    # or: trex> stats --pgid 1',
        '    # PASS: dropped = 0 and average/jitter/max latency look sane for your path.',
        '    # The latency stream keeps its fixed pps and ignores -m.',
        '',
        'NOTES',
        '  - The probe sends ' + opts.srcIp + ' -> ' + opts.dstIp + '; the network must route the',
        '    destination back towards the OTHER TRex port for rx/latency to count.',
        '  - To probe the reverse path, swap src/dst (regenerate or edit in the builder)',
        '    and start with -p 1.'
      ]);
    }

    runbook = runbook.concat([
      '',
      'INTERPRETING FAILURES',
      '  - arp does not resolve: wrong subnet/default_gw in trex_cfg.yaml port_info,',
      '    cabling/VLAN wrong, or the DUT interface is down.',
      '  - ping fails one direction only: the return route is missing on the DUT.',
      '  - ping works but the probe shows rx 0: ICMP is allowed but UDP is dropped -',
      '    check ACLs/policies on the DUT.'
    ]).join('\n') + '\n';

    if (model) { runbook += summarySection(model); }
    files = files.concat([{ name: 'RUNBOOK.txt', language: 'shell', content: runbook }]);
    return { ok: true, model: model, files: files, warnings: warnings };
  }

  /* ---------------- Scenario D: NDR benchmark (./ndr) ---------------- */

  /* Drives the shipped ./ndr wrapper (v3.06 root), which binary-searches for
   * the No-Drop Rate against a RUNNING interactive TRex instance. Flags
   * verified against stl_ndr_bench_tool.py / astf_ndr_bench_tool.py in the
   * local tree: STL needs --ports (even list) and takes -p/--pdr + -e;
   * ASTF REQUIRES --low-mult/--high-mult search bounds.
   *
   * opts: { engine:'stl'|'astf', name, trexVersion,
   *         profileMode:'generate'|'path', profilePath, rate, frameSize,
   *         ports (stl, e.g. "0 1"), pdr, pdrError (stl),
   *         allowedError, lowMult, highMult, latencyPps (astf),
   *         iterTime, maxIterations, qFull, biDir (stl),
   *         maxLatency, latTolerance, outputJson, cores, now } */
  function buildNdr(opts) {
    var errors = [];
    var isStl = opts.engine !== 'astf';
    var ports = String(opts.ports || '0 1').trim().split(/\s+/);
    if (opts.profileMode === 'path' && !opts.profilePath) {
      errors.push('Profile path is required when benchmarking an existing profile.');
    }
    if (opts.profileMode !== 'path' && !(opts.rate > 0)) { errors.push('Profile rate must be greater than 0.'); }
    if (!(opts.iterTime > 0)) { errors.push('Iteration duration must be greater than 0.'); }
    if (!(opts.maxIterations >= 1)) { errors.push('Max iterations must be at least 1.'); }
    if (isStl) {
      if (!ports.length || ports.length % 2 !== 0) { errors.push('Ports must be an even list of port ids, e.g. "0 1".'); }
      if (!(opts.pdr >= 0 && opts.pdr <= 100)) { errors.push('PDR must be between 0 and 100 (% of drops allowed).'); }
    } else {
      if (!(opts.lowMult >= 1)) { errors.push('Low multiplier is required (>= 1) - the ASTF ndr tool has no default.'); }
      if (!(opts.highMult > opts.lowMult)) { errors.push('High multiplier must be above the low multiplier.'); }
    }
    if (opts.latTolerance && !opts.maxLatency) {
      errors.push('Latency tolerance needs a max latency to compare against.');
    }
    if (errors.length) { return { ok: false, errors: errors }; }

    var name = (opts.name || 'ndr_bench').replace(/[^\w.-]+/g, '_');
    var now = opts.now || TB.util.todayIso();
    var cores = opts.cores || 4;
    var model = null;
    var files = [];
    var warnings = [];
    var ranges = { clientStart: '16.0.0.1', clientEnd: '16.0.0.255',
                   serverStart: '48.0.0.1', serverEnd: '48.0.255.255' };

    var profileArg;
    if (opts.profileMode === 'path') {
      profileArg = opts.profilePath;
    } else {
      profileArg = name + '.py';
      if (isStl) {
        model = {
          kind: 'stl', schemaVersion: 1, trexVersion: opts.trexVersion || '3.06',
          meta: { name: name, description: 'generated by scenario wizard', modified: '' },
          tunables: [],
          streams: [stlStream('S0', opts.rate, 0, ranges, opts.frameSize || 64)]
        };
      } else {
        model = astfBase(name, opts.trexVersion, ranges);
        model.mode = 'program';
        model.templates = [httpTemplate(opts.rate, 80, null)];
      }
      var r = generateProfile(model, now);
      if (!r) { return { ok: false, errors: ['No generator registered for TRex v' + model.trexVersion + '.'] }; }
      files = r.files;
      warnings = r.warnings;
    }

    var latPart = '';
    if (opts.maxLatency) {
      latPart = ' --max-latency ' + opts.maxLatency +
        (opts.latTolerance ? ' --lat-tolerance ' + opts.latTolerance : '');
    }
    var outPart = opts.outputJson ? ' -o ' + name + '_results.json' : '';

    var cmd;
    if (isStl) {
      cmd = './ndr --stl --profile ' + profileArg + ' --ports ' + ports.join(' ') +
        ' -p ' + opts.pdr + ' -e ' + (opts.pdrError || 1) +
        ' -t ' + opts.iterTime + ' -x ' + opts.maxIterations + ' -q ' + (opts.qFull || 2) +
        (opts.biDir ? ' --bi-dir' : '') + latPart + outPart + ' -v';
    } else {
      cmd = './ndr --astf --profile ' + profileArg +
        ' --low-mult ' + opts.lowMult + ' --high-mult ' + opts.highMult +
        ' -e ' + (opts.allowedError || 1) +
        ' -t ' + opts.iterTime + ' -x ' + opts.maxIterations + ' -q ' + (opts.qFull || 2) +
        (opts.latencyPps ? ' -lpps ' + opts.latencyPps : '') + latPart + outPart + ' -v';
    }

    var runbook = [
      'TRex NDR benchmark - find the No-Drop Rate automatically (shipped ./ndr tool)',
      'Generated: ' + now + '   Target: TRex v' + (opts.trexVersion || '3.06'),
      '',
      'STEP 1 - start an interactive TRex instance (the ndr tool is a client of it):',
      '    cd <trex dir>',
      '    sudo ./t-rex-64 -i ' + (isStl ? '--stl' : '--astf') + ' --cfg /etc/trex_cfg.yaml -c ' + cores,
      '',
      'STEP 2 - run the benchmark from a second terminal in the SAME directory:',
      (opts.profileMode !== 'path' ? '    # copy ' + name + '.py to the trex directory first' : null),
      '    ' + cmd,
      '',
      'WHAT IT DOES',
      isStl
        ? '  - Binary-searches the transmit rate (as % of line rate) between iterations of'
        : '  - Binary-searches the -m multiplier between ' + opts.lowMult + ' and ' + opts.highMult + ' in iterations of',
      '    ' + opts.iterTime + ' s (max ' + opts.maxIterations + ' iterations), looking for the highest rate where',
      isStl
        ? '    drops stay within ' + opts.pdr + '% (PDR' + (opts.pdr === 0 ? ' = strict NDR' : '') + ', +/- ' + (opts.pdrError || 1) + '% error).'
        : '    drops stay within ' + (opts.allowedError || 1) + '% allowed error.',
      '  - q-full ' + (opts.qFull || 2) + '%: traffic allowed to sit in TX queues before the rate counts as',
      '    above DUT capability.',
      (opts.maxLatency ? '  - Latency gate: a rate also FAILS if latency exceeds ' + opts.maxLatency + ' usec' +
        (opts.latTolerance ? ' for more than ' + opts.latTolerance + '% of probes.' : '.') : null),
      '',
      'READING THE RESULT',
      '  - The tool prints the found rate per iteration and a final table;',
      (opts.outputJson ? '    machine-readable results land in ' + name + '_results.json.' : '    add -o <file>.json for machine-readable results.'),
      isStl
        ? '  - Result is the NDR/PDR in % of line rate, bps and pps for the port pair' + (opts.biDir ? ' (bi-directional).' : '.')
        : '  - Result is the highest stable -m multiplier; NDR cps = profile cps x multiplier.',
      '',
      'NOTES',
      '  - Longer -t iterations give more stable results on bursty DUTs.',
      '  - Do not run other traffic on the instance while the benchmark iterates.'
    ].filter(function (l) { return l !== null; }).join('\n') + '\n';

    if (model) { runbook += summarySection(model); }
    files = files.concat([{ name: 'RUNBOOK.txt', language: 'shell', content: runbook }]);
    return { ok: true, model: model, files: files, warnings: warnings };
  }

  TB.scenarios = {
    buildTwoServer: buildTwoServer,
    buildRamp: buildRamp,
    buildConnCheck: buildConnCheck,
    buildNdr: buildNdr
  };
})(typeof window !== 'undefined' ? window : globalThis);
